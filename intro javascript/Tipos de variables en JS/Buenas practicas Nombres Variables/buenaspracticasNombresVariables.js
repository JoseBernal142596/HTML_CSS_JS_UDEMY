/*Para declarar una variable, vamos a utilizar la palabra reservada let */
/*Cuando el nombre de una variable tiene dos o más palabras, la primera letra de cada palabra se recomienda que esté en mayúsculas (Notación de altas y bajas) */
let nombreCompleto = "Juan Camilo Paez";
console.log(nombreCompleto);

/*También podemos declarar dos o más variables a tiempo y asignarles un valor a cada una de esta manera: */
let x,y;
/*x=45,y=34; (también puede ser de esta forma) */
x=45;
y=34;
let z = x/y;
console.log(x+y);
console.log(z);