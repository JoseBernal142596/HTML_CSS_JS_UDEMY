//Un operador de asignación puede ser el signo = que sirve para asignar un valor a una variable
let a =1;
console.log(a);
//                                               Operador de asignación compuesto
//
a += 3; //Esto es igual a (a= a+3);
console.log(a);

a -=2; // Esto es igual a (a= a-2);
console.log(a); 
//Nota: en este caso disminuye dos unidades (4-2) y es 4-2 ya que en el anterior ejercicio nos dió 4

